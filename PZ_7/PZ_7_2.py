#Дана строка, содержащая полное имя файла. Выделить из этой строки название
#первого каталога (без символов «\»). Если файл содержится в корневом каталоге, то
#вывести символ «\».

file_path = input("Введите полное имя файла: ")
file_path = file_path.strip() #убираем возможные пробелы в начале и конце

if '\\' in file_path: #проверяем, содержит ли путь разделители каталогов
    parts = file_path.strip('\\') #разделяем путь по обратным слешам

    if len(parts) > 1:
        first_catalog = "" #ищем первый непустой элемент после диска(если есть)
        for part in parts:
            if part and not part.endswith(':'): #пропускаем пустые и обозначение диска
                first_catalog = part
                break

        if first_catalog:
            print("Полный путь:", file_path)
            print("Название первого каталога:", first_catalog)
        else:
            print("Полный путь:", file_path)
            print("Файл в корневом каталоге диска: \\")
    else:
        print("Полный путь:", file_path)
        print("Файл в корневом каталоге: \\")
else:
    print("Введённая строка:", file_path)
    print("Файл в корневом каталоге (нет каталогов в пути): \\")
